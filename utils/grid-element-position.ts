export const gridElementPosition = (
  element: string,
  ending?: boolean
): number => {
  let slot = element
  if (slot.length === 4) {
    slot = '0' + slot
  }
  if (ending) {
    if (slot === '00:00') return 50
    if (slot === '00:30') return 51
  }

  return timesToSlots[slot]
}

